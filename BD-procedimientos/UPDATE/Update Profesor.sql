	@ID_PERSONA NUMERIC(10),
	--@ID_PROFESOR NUMERIC(10),
	@NOMBRE_PERSONA VARCHAR(50),--
	@APELLIDO_PATERNO VARCHAR(50),--
	@APELLIDO_MATERNO VARCHAR(50),--
	--@FEC_NACIMIENTO DATE,
	--@EDAD INT,
	@GENERO CHAR(1),--
	@TELEFONO VARCHAR(20),--
	@CORREO_ELECTRONICO VARCHAR(100),--
	@TELEFONO_CONTACTO VARCHAR(20),--
	@NOMBRE_CONTACTO VARCHAR(100),--
	@CORREO_CONTACTO VARCHAR(100),--
	@PROFESION VARCHAR(500),--
	@RESUMEN_CURRICULAR VARCHAR(500),--
	@IND_ACTIVO BIT,--
	--@TIPO_USUARIO VARCHAR(20),--
	@OBSERVACION VARCHAR(500),--
	@ID_PAIS INT,--
	@ID_COMUNA INT,--
	@VILLA VARCHAR(100),--
	@NRO_DPTO VARCHAR(10),--
	@NRO_BLOCK VARCHAR(10),--
	@NRO_CALLE VARCHAR(10),--
	@CALLE VARCHAR(100),--
	--@FEC_CREACION DATETIME,
	--@ULTIMO_ACCESO DATETIME,--
	--@INTENTOS_FALLIDOS INT,--
	@BLOQUEADO_HASTA DATETIME,--
	--@AUD_USUARIO_INGRESO VARCHAR(20),--
	--@AUD_FEC_INGRESO DATETIME,--
	@AUD_USUARIO_MODIFICA_PT VARCHAR(20),--
	--@AUD_FEC_MODIFICA_PT DATETIME,--
	@AUD_USUARIO_MODIFICA_P VARCHAR(20)
	--@AUD_FEC_MODIFICA_P DATETIME

	-- ALGO QUE ENTENDER PRIMERO
	-- en la bd existen unas cuantas tablas pero las mas importantes son la persona_taller, taller, profesor. hasta aqui llegan las brigidas pero ahi un detalle
	-- el detalle es que existen tablas como gestion_taller y cadelarizacion_taller. gestion junta al profe con el taller  ya que persona y profe comparten id persona
	-- pero taller solo tiene id taller , gestion cuenta con id taller y id profesor, ademas profe solo tiene id persona y id profe
	-- entonces se crea un puente entre profesor y taller a traves de gestion, mientras cadelarizacion_taller es para lo que dice.
	-- cadelarizacion_taller deberia de cambiar cada vez que ocurre dicha accion

AS BEGIN
	SET NOCOUNT ON;

	BEGIN TRY
		BEGIN TRANSACTION;

		UPDATE PT
		-- SI EN ESTA TABLA SE ALTERA ALGUN DATO , FECHA DE MODIFICACION DEBERIA ACTUALIZARSE Y USUARIO TAMBIEN DEBERIA ACTUALIZARSE
		-- podria hacer eso la vrd pero por ahora lo mantendre asi , aun que no se cambie nada apenas se le de guardar esta tabla debera actualizar el tema del usuario y el tema de la fecha modifica
		SET PT.NOMBRE_PERSONA=ISNULL(@NOMBRE_PERSONA, '-'),
			PT.APELLIDO_PATERNO=ISNULL(@APELLIDO_PATERNO, '-'),
			PT.APELLIDO_MATERNO=ISNULL(@APELLIDO_MATERNO, '-'),
			PT.GENERO=ISNULL(@GENERO, 'O'),
			PT.TELEFONO=ISNULL(@TELEFONO, '-'),
			PT.CORREO_ELECTRONICO=ISNULL(@CORREO_ELECTRONICO, '-'),
			PT.TELEFONO_CONTACTO=ISNULL(@TELEFONO_CONTACTO, '-'),
			PT.NOMBRE_CONTACTO=ISNULL(@NOMBRE_CONTACTO, '-'),
			PT.CORREO_CONTACTO=ISNULL(@CORREO_CONTACTO, '-'),
			PT.IND_ACTIVO=@IND_ACTIVO,
			--PT.TIPO_USUARIO=CASE WHEN @TIPO_USUARIO = 'FUNCIONARIO' THEN 'FUNCIONARIO' WHEN @TIPO_USUARIO = 'PROFESOR' THEN 'PROFESOR' ELSE 'ESTUDIANTE' END,
			--PT.TIPO_USUARIO=CASE WHEN @TIPO_USUARIO = 'PROFESOR' THEN 'PROFESOR' ELSE 'ESTUDIANTE' END,
			PT.OBSERVACION=ISNULL(@OBSERVACION, '-'),
			PT.ID_PAIS=@ID_PAIS,
			PT.ID_COMUNA=@ID_COMUNA,
			PT.VILLA=ISNULL(@VILLA, '-'),
			PT.NRO_DPTO=ISNULL(@NRO_DPTO, '-'),
			PT.NRO_BLOCK=ISNULL(@NRO_BLOCK, '-'),
			PT.NRO_CALLE=ISNULL(@NRO_CALLE, '-'),
			PT.CALLE=ISNULL(@CALLE, '-'),
			--PT.ULTIMO_ACCESO=@ULTIMO_ACCESO,
			--PT.INTENTOS_FALLIDOS=@INTENTOS_FALLIDOS,
			PT.BLOQUEADO_HASTA=CONVERT(DATE, @BLOQUEADO_HASTA, 121),
			--PT.AUD_USUARIO_INGRESO=@AUD_USUARIO_INGRESO,
			--PT.AUD_FEC_INGRESO=@AUD_FEC_INGRESO,
			PT.AUD_USUARIO_MODIFICA=@AUD_USUARIO_MODIFICA_PT,
			PT.AUD_FEC_MODIFICA=GETDATE()
		FROM SGT_PERSONA_TALLER PT
		WHERE PT.ID_PERSONA=@ID_PERSONA;

	--ID_CATEGORIA=@ID_CATEGORIA,
	--NOMBRE_TALLER=@NOMBRE_TALLER,
	--OBJETIVO_TALLER=@OBJETIVO_TALLER,
	--FEC_INICIO=CONVERT(DATE, @FEC_INICIO, 111),
	--FEC_TERMINO=CONVERT(DATE, @FEC_TERMINO, 111),
	--NRO_MINUTOS=@NRO_MINUTOS,
	--NRO_CLASES_ANUAL=@NRO_CLASES_ANUAL,
	--HORAS_TOTALES=@HORAS_TOTALES,
	--ID_ESTADO_TALLER=@ID_ESTADO_TALLER,
	--FEC_ESTADO_TALLER=CASE WHEN ID_ESTADO_TALLER!=@ID_ESTADO_TALLER THEN GETDATE() ELSE FEC_ESTADO_TALLER END,
	--OBSERVACION=ISNULL(@OBSERVACION, '-'),
	--LUGAR=ISNULL(@LUGAR, '-'),
	--MINIMO_ESTUDIANTE=@MINIMO_ESTUDIANTE,
	--MAXIMO_ESTUDIANTE=@MAXIMO_ESTUDIANTE,
	--REQUISITO=ISNULL(@REQUISITO, '-'),
	--EDAD_MINIMA=@EDAD_MINIMA,
	--EDAD_MAXIMA=@EDAD_MAXIMA,
	--MATERIAL=ISNULL(@MATERIAL, '-'),
	--IND_TIPO_TALLER=@IND_TIPO_TALLER,
	--AUD_USUARIO_MODIFICA=@AUD_USUARIO_MODIFICA,
	--AUD_FEC_MODIFICA=GETDATE()

		UPDATE P
		-- EN ESTA TABLA TAMBIEN DEBERIA EXISTIR ALTERACIONES SOBRE EL TEMA FECHA Y USUARIO MODIFICA
		SET P.PROFESION=@PROFESION,
			P.RESUMEN_CURRICULAR=@RESUMEN_CURRICULAR,
			P.AUD_USUARIO_MODIFICA=@AUD_USUARIO_MODIFICA_P,
			P.AUD_FEC_MODIFICA=GETDATE()
		FROM SGT_PROFESOR P
		WHERE P.ID_PERSONA=@ID_PERSONA;

		COMMIT TRANSACTION;
	END TRY
	BEGIN CATCH
		IF @@TRANCOUNT >0
			ROLLBACK TRANSACTION;
	END CATCH
END;
